def main():
    args = parse_args()
    set_seed(args.seed)
    pl.seed_everything(args.seed, workers=True)

    torch.set_float32_matmul_precision("high")

    # Validate paths
    if not args.train_pairs.exists():
        raise SystemExit(f"Train pairs file not found: {args.train_pairs}")
    if not args.val_pairs.exists():
        raise SystemExit(f"Val pairs file not found: {args.val_pairs}")

