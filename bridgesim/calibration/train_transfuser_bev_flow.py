"""
PyTorch Lightning training scaffold for TransFuser BEV flow matching.

Key behaviors:
- Loads MetaDrive/NavSim token pairs from separate train/val JSONL files.
- Loads precomputed BEV embeddings from H5 cache files.
- Runs a FlowMatching model to learn BEV-to-BEV transformations.
- Source domain: always MetaDrive
- Target domain: navsim or navsim_no_lidar (configurable)

Assumptions:
- BEV cache must be precomputed using precompute_transfuser_bev_cache.py.
- Token pairs generated by build_transfuser_bev_pairs.py.
- TransFuser BEV shape: (512, 8, 8)
"""

