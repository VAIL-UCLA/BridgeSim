class PairDataModule(pl.LightningDataModule):
    def __init__(
        self,
        pairs_path: Path,
        split_path: Path,
        cache_dir: Path,
        batch_size: int = 1,
        num_workers: int = 4,
        max_samples: Optional[int] = None,
    ) -> None:
        super().__init__()
        self.pairs_path = pairs_path
        self.split_path = split_path
        self.cache_dir = cache_dir
        self.batch_size = batch_size
        self.num_workers = num_workers
        self.train_bundles: List[FrameBundle] = []
        self.val_bundles: List[FrameBundle] = []
        self.max_samples = max_samples

    def setup(self, stage: Optional[str] = None) -> None:
        bundles = load_frame_bundles(self.pairs_path)
        val_set = load_split(self.split_path)
        self.train_bundles, self.val_bundles = split_bundles(bundles, val_set)
        if self.max_samples is not None:
            self.train_bundles = self.train_bundles[:self.max_samples]

    def train_dataloader(self):
        return DataLoader(
            PairDataset(
                self.train_bundles,
                cache_dir=self.cache_dir,
            ),
            batch_size=self.batch_size,
            shuffle=True,
            num_workers=self.num_workers,
            collate_fn=collate_fn,
            pin_memory=True,
            persistent_workers=(self.num_workers > 0),
        )

    def val_dataloader(self):
        return DataLoader(
            PairDataset(
                self.val_bundles,
                cache_dir=self.cache_dir,
            ),
            batch_size=self.batch_size,
            shuffle=False,
            num_workers=self.num_workers,
            collate_fn=collate_fn,
            pin_memory=True,
            persistent_workers=(self.num_workers > 0),
        )


