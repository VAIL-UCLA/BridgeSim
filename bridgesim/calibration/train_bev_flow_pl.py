"""
PyTorch Lightning training scaffold for UniAD BEV flow matching (single-node multi-GPU).

Key behaviors:
- Loads MetaDrive/Bench2Drive frame pairs and splits using a provided val list;
  everything else is treated as train.
- Loads precomputed BEV embeddings from cache (no on-the-fly BEV extraction).
- Runs a FlowMatching model to learn BEV-to-BEV transformations.
- Optional visualization during test (detections + map/occ overlays) on global rank 0.

Assumptions:
- Uses high precision (no AMP).
- BEV cache must be precomputed using precompute_temporal_bev_cache.py.
- Frame pairs generated by build_bench2drive_metadrive_frame_pairs.py.
- UniAD model loaded only for test visualization.
"""

