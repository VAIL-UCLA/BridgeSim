@dataclass
class FrameBundle:
    """Minimal frame bundle for cache lookup - only needs scenario and frame."""
    scenario: str
    frame: str


def load_frame_bundles(path: Path) -> List[FrameBundle]:
    """Load frame bundles from JSONL, extracting only scenario and frame for cache lookup."""
    bundles: List[FrameBundle] = []
    with path.open("r", encoding="utf-8") as f:
        for line_no, raw in enumerate(f, start=1):
            line = raw.strip()
            if not line:
                continue
            payload = json.loads(line)
            try:
                bundles.append(
                    FrameBundle(
                        scenario=str(payload["scenario"]),
                        frame=str(payload["frame"]),
                    )
                )
            except KeyError as exc:
                raise ValueError(f"Missing field {exc} on line {line_no}") from exc
    return bundles


def set_seed(seed: int) -> None:
    random.seed(seed)
    torch.manual_seed(seed)
    torch.cuda.manual_seed_all(seed)


def build_optimizer(params, name: str, lr: float, weight_decay: float = 0.0):
    name = name.lower()
    if name == "adam":
        return torch.optim.Adam(params, lr=lr, weight_decay=weight_decay)
    if name == "adamw":
        return torch.optim.AdamW(params, lr=lr, weight_decay=weight_decay)
    raise ValueError(f"Unsupported optimizer {name}")


def build_lr_scheduler(optimizer, name: str, *, warmup_steps: int = -1, t_max: int = 100):
    name = name.lower()
    if name == "none":
        return None
    if name == "customized":
        def fn(step: int):
            if warmup_steps > 0:
                return min(step / warmup_steps, 1.0)
            return 1.0
        return LambdaLR(optimizer, fn)
    if name == "cosine":
        return CosineAnnealingLR(optimizer, T_max=t_max)
    raise ValueError(f"Unsupported lr scheduler {name}")


def load_split(split_path: Path) -> set:
    with split_path.open("r", encoding="utf-8") as f:
        data = json.load(f)
    return set(data.get("val", []))


def split_bundles(bundles: List[FrameBundle], val_set: set) -> Tuple[List[FrameBundle], List[FrameBundle]]:
    train_b, val_b = [], []
    for b in bundles:
        if f"v1/{b.scenario}" in val_set:
            val_b.append(b)
        else:
            train_b.append(b)
    return train_b, val_b


class PairDataset(Dataset):
    def __init__(
        self,
        bundles: List[FrameBundle],
        cache_dir: Path,
    ):
        self.bundles = bundles
        self.cache_dir = cache_dir

    def __len__(self) -> int:
        return len(self.bundles)

    def __getitem__(self, idx: int):
        bundle = self.bundles[idx]

        # Load cached BEV for MetaDrive
        md_cache_path = self.cache_dir / "metadrive" / bundle.scenario / f"{bundle.frame}.pt"
        if not md_cache_path.exists():
            return None

        # Load cached BEV for Bench2Drive
        b2d_cache_path = self.cache_dir / "bench2drive" / bundle.scenario / f"{bundle.frame}.pt"
        if not b2d_cache_path.exists():
            return None

        try:
            # Load and validate MetaDrive BEV
            md_loaded = torch.load(md_cache_path, map_location="cpu", weights_only=True)
            if isinstance(md_loaded, dict):
                if md_loaded.get("scenario") != bundle.scenario or md_loaded.get("frame") != bundle.frame:
                    return None
                md_bev = md_loaded["bev"]
            else:
                md_bev = md_loaded

            # Load and validate Bench2Drive BEV
            b2d_loaded = torch.load(b2d_cache_path, map_location="cpu", weights_only=True)
            if isinstance(b2d_loaded, dict):
                if b2d_loaded.get("scenario") != bundle.scenario or b2d_loaded.get("frame") != bundle.frame:
                    return None
                b2d_bev = b2d_loaded["bev"]
            else:
                b2d_bev = b2d_loaded

            # Reshape precomputed cache tensors to expected format
            # Precomputed cache can have shapes like (H*W, 1, C) or (1, C, H, W)
            # Ensure they are squeezed to remove batch dimension of 1
            if md_bev.dim() == 3 and md_bev.shape[1] == 1:
                # Shape: (H*W, 1, C) -> (H*W, C)
                md_bev = md_bev.squeeze(1)
            elif md_bev.dim() == 4 and md_bev.shape[0] == 1:
                # Shape: (1, C, H, W) -> (C, H, W)
                md_bev = md_bev.squeeze(0)

            if b2d_bev.dim() == 3 and b2d_bev.shape[1] == 1:
                # Shape: (H*W, 1, C) -> (H*W, C)
                b2d_bev = b2d_bev.squeeze(1)
            elif b2d_bev.dim() == 4 and b2d_bev.shape[0] == 1:
                # Shape: (1, C, H, W) -> (C, H, W)
                b2d_bev = b2d_bev.squeeze(0)

            return bundle, md_bev, b2d_bev

        except Exception:
            return None


def collate_fn(batch: List):
    batch = [b for b in batch if b is not None]
    if not batch:
        return None
    bundles, md_bevs, b2d_bevs = zip(*batch)
    # Stack BEV tensors into batches
    md_bev_batch = torch.stack(md_bevs, dim=0)  # (B, ...)
    b2d_bev_batch = torch.stack(b2d_bevs, dim=0)  # (B, ...)
    return list(bundles), md_bev_batch, b2d_bev_batch


class FlowMatchingLit(pl.LightningModule):
    def __init__(
        self,
        lr: float = 1e-4,
        optimizer_name: str = "adamw",
        weight_decay: float = 0.01,
        lr_scheduler_name: str = "customized",
        warmup_steps: int = 500,
        lr_t_max: int = 1000,
        ema_decay: float = 0.9999,
        visualize: bool = False,
        viz_dir: Optional[Path] = None,
        viz_score_thresh: float = 0.2,
        config_path: Optional[Path] = None,
        checkpoint_path: Optional[Path] = None,
        dit_variant: str = "DiT-B/2",
    ) -> None:
        super().__init__()
        self.save_hyperparameters()
        self.lr = lr
        self.optimizer_name = optimizer_name
        self.weight_decay = weight_decay
        self.lr_scheduler_name = lr_scheduler_name
        self.warmup_steps = warmup_steps
        self.lr_t_max = lr_t_max
        self.ema_decay = ema_decay
        self.visualize_flag = visualize
        self.viz_dir = viz_dir or Path("bev_viz_pl")
        self.viz_score_thresh = viz_score_thresh
        self.config_path = config_path
        self.checkpoint_path = checkpoint_path

        # Flow model (DiT-backed)
        self.flow_model = FlowMatchingModel(dit_variant=dit_variant)
        self.ema_flow_model = deepcopy(self.flow_model)
        for p in self.ema_flow_model.parameters():
            p.requires_grad_(False)

        # UniAD model for visualization only (loaded lazily if needed)
        self.uni_model = None

    def setup(self, stage: Optional[str] = None) -> None:
        # Only load UniAD if visualization is enabled
        if self.visualize_flag and self.uni_model is None and self.config_path and self.checkpoint_path:
            uni_model, _ = load_model_and_pipeline(str(self.config_path), str(self.checkpoint_path))
            for p in uni_model.parameters():
                p.requires_grad_(False)
            uni_model.eval()
            self.uni_model = uni_model.to(self.device)

    def on_validation_epoch_end(self):
        # Force cleanup after validation
        gc.collect()
        torch.cuda.empty_cache()

    def configure_optimizers(self):
        optimizer = build_optimizer(
            self.flow_model.parameters(),
            name=self.optimizer_name,
            lr=self.lr,
            weight_decay=self.weight_decay,
        )
        scheduler = build_lr_scheduler(
            optimizer,
            name=self.lr_scheduler_name,
            warmup_steps=self.warmup_steps,
            t_max=self.lr_t_max,
        )
        if scheduler is None:
            return optimizer
        return {
            "optimizer": optimizer,
            "lr_scheduler": {
                "scheduler": scheduler,
                "interval": "step",
            },
        }

    @torch.no_grad()
    def _ema_update(self):
        if self.ema_flow_model is None:
            return
        ema_params = dict(self.ema_flow_model.named_parameters())
        for name, param in self.flow_model.named_parameters():
            ema_param = ema_params[name]
            ema_param.data.mul_(self.ema_decay).add_((1 - self.ema_decay) * param.data)

    def _flow_step(self, bundles, md_bev: torch.Tensor, b2d_bev: torch.Tensor, stage: str):
        """
        Shared flow-matching computation for train/val/test.
        """
        src_bchw = self.flow_model.reshape_bev(md_bev)
        tgt_bchw = self.flow_model.reshape_bev(b2d_bev)
        t = torch.rand(src_bchw.shape[0], device=self.device)

        x_t = self.flow_model.psi(t, noise=src_bchw, x1=tgt_bchw)
        target_velocity = self.flow_model.dt_psi(t, noise=src_bchw, x1=tgt_bchw)

        v_pred = self.flow_model(x_t, t)
        loss = (v_pred - target_velocity).pow(2).flatten(1).mean()

        log_name = f"{stage}/loss"
        self.log(
            log_name,
            loss,
            prog_bar=True,
            on_step=(stage == "train"),
            on_epoch=True,
            sync_dist=True,
        )
        return loss

    def optimizer_step(self, *args, **kwargs):
        super().optimizer_step(*args, **kwargs)
        self._ema_update()

