    parser.add_argument("--epochs", type=int, default=100)
    parser.add_argument("--lr", type=float, default=1e-4)
    parser.add_argument("--optimizer", type=str, default="adamw", choices=["adam", "adamw"], help="Optimizer type.")
    parser.add_argument("--weight-decay", type=float, default=0.01)
    parser.add_argument("--lr-scheduler", type=str, default="customized", choices=["none", "customized", "cosine"])
    parser.add_argument("--warmup-steps", type=int, default=500, help="Warmup steps for customized scheduler.")
    parser.add_argument("--lr-t-max", type=int, default=1000, help="T_max for cosine scheduler.")
